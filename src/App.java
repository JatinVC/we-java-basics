// class --> data and functions all together.
// function --> named block that has repeated code that is executed.
/**  static --> not associated with an object, don't need
                to create an object to call the function */
// void --> no return value in the function.
// String[] args --> holds input arguments from the compiler.

public class App {
    // the main method, only things in here will be executed.
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");
        System.out.println("Once more");
    }
}